    {
	    $customer = new Customer();
	    $data = $this->validate($request, [
		    'name'=>'required',
		    'contact'=> 'required',
		    'email'=> 'required',
		    'phone'=> 'required'
	    ]);

	    $customer->saveCustomer($data);
	    return redirect('/home')->with('success', 'Klant is toegevoegd!');
    }

